package org.shypl.common.util;

public final class ArrayUtils {
	public static int sum(int[] array) {
		int sum = 0;
		for (int e : array) {
			sum += e;
		}
		return sum;
	}

	public static long sum(long[] array) {
		long sum = 0;
		for (long e : array) {
			sum += e;
		}
		return sum;
	}

	public static long sumLong(int[] array) {
		long sum = 0;
		for (int e : array) {
			sum += e;
		}
		return sum;
	}
}
